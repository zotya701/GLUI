using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace GLUI.Foundation.MathExtension
{
    public class Point2
    {
        public int X { get; set; } = 0;
        public int Y { get; set; } = 0;

        public Point2(int X, int Y)
        {
            this.X = X;
            this.Y = Y;
        }
    }
